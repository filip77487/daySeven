package daySeven;

public class IrishWhiskeySwitch {

	public static void main(String[] args) {
		int counter = 1;
		
		
		
		switch (counter) {
		case 1:
			System.out.println("give me one glass of whiskey");
			
		case 2:
			System.out.println("give me two glasses of whiskey");
			break;
		case 3:
			System.out.println("give me three glasses of whiskey");
			break;
		default:
			System.out.println("give me one bottle of whiskey");
			break;

	}

}
}
